#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*tmp;

	tmp = malloc(count * size);
	if (!tmp)
		return (NULL);
	tmp = ft_memset(tmp, 0, count * size);
	return (tmp);
}
